/**
 * Fetches country data, picks a sensible primary IANA zone per ISO2,
 * and writes src/lib/countryTimezone.generated.ts (country map + full timezone list for UI).
 *
 * Run: node scripts/gen-country-timezones.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '..', 'src', 'lib', 'countryTimezone.generated.ts');

/** Multi-zone countries where capital-matching is wrong or ambiguous. */
const PRIMARY_BY_ISO2 = {
  US: 'America/New_York',
  AU: 'Australia/Sydney',
  CA: 'America/Toronto',
  BR: 'America/Sao_Paulo',
  MX: 'America/Mexico_City',
  RU: 'Europe/Moscow',
  KZ: 'Asia/Almaty',
  FM: 'Pacific/Chuuk',
  PS: 'Asia/Gaza',
  CD: 'Africa/Kinshasa',
  CN: 'Asia/Shanghai',
  IN: 'Asia/Kolkata',
  ID: 'Asia/Jakarta',
  MY: 'Asia/Kuala_Lumpur',
  ES: 'Europe/Madrid',
  PT: 'Europe/Lisbon',
};

function pickPrimary(country) {
  const iso = country.iso2;
  if (PRIMARY_BY_ISO2[iso]) return PRIMARY_BY_ISO2[iso];

  let zones = (country.timezones ?? []).map((t) => t.zoneName);
  zones = zones.filter((z) => iso === 'AQ' || !z.startsWith('Antarctica/'));
  if (!zones.length) return country.timezones?.[0]?.zoneName ?? null;
  if (zones.length === 1) return zones[0];

  const cap = country.capital?.[0];
  if (cap) {
    const compact = cap.split(',')[0].toLowerCase().replace(/[^a-z]/g, '');
    if (compact.length > 3) {
      for (const z of zones) {
        if (z.toLowerCase().replace(/[^a-z]/g, '').includes(compact)) return z;
      }
    }
    const capWords = cap
      .split(/[,&]/)[0]
      .trim()
      .toLowerCase()
      .split(/\s+/)
      .map((w) => w.replace(/[^a-z]/g, ''))
      .filter((w) => w.length > 2);
    for (const w of capWords) {
      const hit = zones.find((z) => z.toLowerCase().replace(/_/g, '').includes(w));
      if (hit) return hit;
    }
  }

  return zones[0];
}

/** Match existing UI style: GMT, GMT-5, GMT+5:30 */
function compactOffsetLabel(zone, ref) {
  try {
    const parts = new Intl.DateTimeFormat('en-US', {
      timeZone: zone,
      timeZoneName: 'longOffset',
    }).formatToParts(ref);
    const gmt = parts.find((p) => p.type === 'timeZoneName')?.value ?? 'GMT';
    if (!gmt.startsWith('GMT')) return 'GMT';
    const rest = gmt.slice(3).trim();
    if (!rest) return 'GMT';
    const m = rest.match(/^([+-])(\d{1,2})(?::(\d{2}))?$/);
    if (!m) return `GMT${rest}`;
    const sign = m[1];
    const h = parseInt(m[2], 10);
    const min = parseInt(m[3] ?? '0', 10);
    if (min === 0) return `GMT${sign}${h}`;
    return `GMT${sign}${h}:${String(min).padStart(2, '0')}`;
  } catch {
    return 'GMT';
  }
}

const res = await fetch(
  'https://raw.githubusercontent.com/dr5hn/countries-states-cities-database/master/json/countries.json',
);
if (!res.ok) throw new Error(`Fetch failed: ${res.status}`);
const data = await res.json();

/** @type {Record<string, string>} */
const byIso = {};
for (const c of data) {
  const z = pickPrimary(c);
  if (c.iso2 && z) byIso[c.iso2] = z;
}

const ref = new Date('2025-06-15T12:00:00Z');
const uniqueZones = [...new Set(Object.values(byIso))].sort((a, b) => a.localeCompare(b));
if (!uniqueZones.includes('Etc/UTC')) uniqueZones.unshift('Etc/UTC');
else {
  const i = uniqueZones.indexOf('Etc/UTC');
  uniqueZones.splice(i, 1);
  uniqueZones.unshift('Etc/UTC');
}

const options = uniqueZones.map((zone) => ({
  zone,
  offset: compactOffsetLabel(zone, ref),
}));

let out = `/**\n * Auto-generated by scripts/gen-country-timezones.mjs — do not edit by hand.\n * Primary IANA zone per ISO 3166-1 alpha-2 + all such zones for the timezone picker.\n */\n\n`;
out += `export const DEFAULT_TIMEZONE_BY_ISO2: Record<string, string> = {\n`;
for (const k of Object.keys(byIso).sort()) {
  const z = byIso[k].replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  out += `  '${k}': '${z}',\n`;
}
out += `};\n\n`;

out += `export const GENERATED_COMMON_TIMEZONES: { zone: string; offset: string }[] = [\n`;
for (const o of options) {
  const z = o.zone.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const off = o.offset.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  out += `  { zone: '${z}', offset: '${off}' },\n`;
}
out += `];\n`;

fs.writeFileSync(outPath, out, 'utf8');
console.log('Wrote', outPath);
console.log('Countries:', Object.keys(byIso).length, 'unique zones:', uniqueZones.length);
